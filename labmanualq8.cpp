#include <iostream>
using namespace std;

int main() {
    int N;

    cout << "Enter value of N: ";
    cin >> N;

    for(int i = 1; i <= N; i++) {
        
        if(i % 3 == 0 && i % 5 == 0) {
            cout << "BuzzFuzz";
        }
        else if(i % 3 == 0) {
            cout << "Buzz";
        }
        else if(i % 5 == 0) {
            cout << "Fuzz";
        }
        else {
            cout << i;
        }

        cout << endl; // move to next line
    }

    return 0;
}
